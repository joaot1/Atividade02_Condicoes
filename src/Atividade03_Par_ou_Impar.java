public class Atividade03_Par_ou_Impar {
    public static void main(String[] args) {

        int numero = 6;

        // Verificação de Numero Par ou Ímpar
        if (numero % 2 == 0) {
            System.out.println("O número " + numero + " é par.");
        } else {
            System.out.println("O número " + numero + " é impar.");
        }
    }
}
